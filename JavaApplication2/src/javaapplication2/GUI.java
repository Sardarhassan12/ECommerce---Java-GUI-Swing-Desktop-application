    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
           
        String customerAddress = deliveryAddress.getText();
        String paymentType = (String) payType.getSelectedItem();
        double totalPaymentOrder;

        try {
            totalPaymentOrder = Double.parseDouble(orderTotalPayment.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid payment amount", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (customerAddress.isEmpty() || paymentType.isEmpty() || totalPaymentOrder <= 0) {
            JOptionPane.showMessageDialog(this, "Some information is not correct", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        ArrayList<Integer> productIds = new ArrayList<>();
        ArrayList<Integer> quantities = new ArrayList<>();

        for (int i = 0; i < cartItemTable.getRowCount(); i++) {
            try {
                Object productIdObj = cartItemTable.getValueAt(i, 0);
                Object quantityObj = cartItemTable.getValueAt(i, 2);

                if (productIdObj == null || quantityObj == null) {
                    throw new NumberFormatException("Null value found in table");
                }
                String productIdStr = productIdObj.toString().trim();
                String quantityStr = quantityObj.toString().trim();

                if (productIdStr.isEmpty() || quantityStr.isEmpty()) {
                    throw new NumberFormatException("Empty value found in table");
                }

                int productId = Integer.parseInt(productIdStr);
                int quantity = Integer.parseInt(quantityStr);

                productIds.add(productId);
                quantities.add(quantity);

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Invalid data in cart: row " + (i + 1), "Error", JOptionPane.ERROR_MESSAGE);
                
                return;
            }
        }

        if (productIds.isEmpty() || quantities.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Your cart is empty", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Customer customer = (Customer) existingUser;
        Order order = new Order(customer.getUserName(), customerAddress, paymentType, totalPaymentOrder);

        boolean success = order.addOrder(connection, productIds, quantities);
        if (success) {
            JOptionPane.showMessageDialog(this, "Order placed successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

            
            DefaultTableModel model = (DefaultTableModel) cartItemTable.getModel();
            model.setRowCount(0); 

            orderTotalPayment.setText("0.00");

        } else {
            JOptionPane.showMessageDialog(this, "Failed to place order", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

